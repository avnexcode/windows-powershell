function Show-Version {
    param (
        [string]$ToolName,
        [string]$Version
    )
    Show-LargeText
    $lineLength = $ToolName.Length + $Version.Length + 11
    $line = "-" * $lineLength
    Write-Host $line
    Write-Host -NoNewline "• "
    Write-Host -NoNewline $ToolName -ForegroundColor Green
    Write-Host -NoNewline " Version "
    Write-Host $Version -ForegroundColor Yellow
    Write-Host $line
}

